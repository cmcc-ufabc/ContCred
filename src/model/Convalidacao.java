package model;

public class Convalidacao {
	private int id;
	private String cod_disciplina;
	private String cod_convalidacao;
	private int cod_ppc;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCod_disciplina() {
		return cod_disciplina;
	}
	public void setCod_disciplina(String cod_disciplina) {
		this.cod_disciplina = cod_disciplina;
	}
	public String getCod_convalidacao() {
		return cod_convalidacao;
	}
	public void setCod_convalidacao(String cod_convalidacao) {
		this.cod_convalidacao = cod_convalidacao;
	}
	public int getCod_ppc() {
		return cod_ppc;
	}
	public void setCod_ppc(int cod_ppc) {
		this.cod_ppc = cod_ppc;
	}
}
